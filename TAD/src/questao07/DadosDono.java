package questao07;

public class DadosDono {
	
	String nomeDono;
	int telefoneDono;
	
	public DadosDono(String nomeDono, int telefoneDono) {
		this.nomeDono = nomeDono;
		this.telefoneDono = telefoneDono;
	}
	
	public String getNomeDono() {
		return nomeDono;
	}
	public void setNomeDono(String nomeDono) {
		this.nomeDono = nomeDono;
	}
	public int getTelefoneDono() {
		return telefoneDono;
	}
	public void setTelefoneDono(int telefoneDono) {
		this.telefoneDono = telefoneDono;
	}
	
	
}
